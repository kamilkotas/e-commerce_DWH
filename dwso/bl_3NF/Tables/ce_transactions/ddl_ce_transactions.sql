CREATE SCHEMA IF NOT EXISTS bl_3nf;

CREATE TABLE IF NOT EXISTS bl_3nf.ce_transactions (
					promotion_id		BIGINT NOT NULL,
					customer_id			BIGINT NOT NULL,
					shop_id				BIGINT NOT NULL,
					employee_id			BIGINT NOT NULL,
					channel_id			BIGINT NOT NULL,
					product_id			BIGINT NOT NULL,
					cots				NUMERIC(10, 2),
					price				NUMERIC(10, 2),
					transaction_no		VARCHAR(255) NOT NULL,
					transaction_id		VARCHAR(255) NOT NULL,
					quantity			INTEGER,
					date_of_transaction DATE NOT NULL,
					update_dt			DATE NOT NULL,
					insert_dt			DATE NOT NULL,
				CONSTRAINT transaction_shop_id_id_fkey FOREIGN KEY (shop_id) REFERENCES bl_3nf.ce_shops(shop_id),
				CONSTRAINT transaction_promotion_id_fkey FOREIGN KEY (promotion_id) REFERENCES bl_3nf.ce_promotions_scd2(promotion_id),
				CONSTRAINT transaction_customer_id_fkey FOREIGN KEY (customer_id) REFERENCES bl_3nf.ce_customers(customer_id),
				CONSTRAINT transaction_employee_id_fkey FOREIGN KEY (employee_id) REFERENCES bl_3nf.ce_employees(employee_id),
				CONSTRAINT transaction_channel_id_fkey FOREIGN KEY (channel_id) REFERENCES bl_3nf.ce_channels(channel_id),
				CONSTRAINT transaction_product_id_fkey FOREIGN KEY (product_id) REFERENCES bl_3nf.ce_products(product_id)
				);
				
